package LOOPS;
import java.util.Scanner;
public class For_loop {

	public static void main(String[] args) {
         int n;
         Scanner sc=new Scanner(System.in);
         System.out.println("ENTER THE VALUE of n");
         n=sc.nextInt();
         for (int i=0;i<n;i++) {
        	 System.out.println(+i);
         }

        sc.close(); 

        		 
         }
         
        	 
	
         
	}
	




